    private void auditTable(){
         try {
             String sql ="select * from Audit";
             pst = conn.prepareStatement(sql);
             rs=pst.executeQuery();
             jTable1.setModel(DbUtils.resultSetToTableModel(rs));
         } catch (SQLException ex) {
             JOptionPane.showMessageDialog(null, ex);
         }finally{
         
             try {
                 rs.close();
                 pst.close();
                 conn.close();
             } catch (Exception e) {
             }
         }
    
    }
